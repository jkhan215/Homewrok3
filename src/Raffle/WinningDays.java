public class WinningDays {

		public void day(int choice){
			String day = "";
			switch(choice){
			
			case 1: day = "Monday";
			System.out.println("Today winning amount is 10,000");
			break;
			case 2: day = "Tuesday";
			System.out.println("Today amount is 15,000");
			break;
			case 3: day = "Wednesday";
			System.out.println("Today amount is 20,000");
			
			
			
				
			}
		}
}
